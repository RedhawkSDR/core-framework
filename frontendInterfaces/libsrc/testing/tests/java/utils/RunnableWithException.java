package utils;

/**
 * Equivalent to java.lang.Runnable, but declared to throw all exceptions. This
 * is useful for testing that the correct exception is thrown without going
 * through a lot of boilerplate try/catch or JUnit's expected exceptions.
 */
@FunctionalInterface
public interface RunnableWithException {
    public void run() throws Exception;
}
